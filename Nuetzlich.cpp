bool leseZahl(Zeichenkette& zk,uint64_t& zahl)
{
    zahl = 0;
    if( zk.laenge() == 0)
    {
       return false;
    }
    char zeichen;
    uint8_t stelle(0);
    do
    {
       zeichen = zk[stelle++];
       if( istZiffer(zeichen) )
       {
          zahl += (zeichen - '0');
       }
       else return false;
       if( stelle < zk.laenge() )
       {
           zahl *= 10;
       }
       else break;
    }
    while( true );
    return true;
}
